@mcp.tool()
def get_all_trips(token: str):
    """this tool return all the trips in system, it requires token that is generated by login tool.
    trip is the main entity of travel which combine bus and route to make a one trip"""
    
    return get_all_trips_tool(token)

@mcp.tool()
def delete_trips(token: str, TripId: str):
    """this tool delete the trips based on TripId ."""
    
    return delete_trips_tool(token,TripId)

@mcp.tool()
def find_available_resources(
    route_id: str,
    departure_time: str,
    days: list[str],
    token: str
):
    """
    Returns available buses, drivers, and conductors for given route, time and days.
    useful before creating or updating a trip 

    days must be weekday names (3-letter lowercase):
    ["sun","mon","tue","wed","thu","fri","sat"]

    Examples:
    ["mon","fri"]
    ["sat","sun"]

    departure_time: "HH:MM AM/PM"
    """
    
    return find_available_resources_tool(route_id,departure_time,days,token)

